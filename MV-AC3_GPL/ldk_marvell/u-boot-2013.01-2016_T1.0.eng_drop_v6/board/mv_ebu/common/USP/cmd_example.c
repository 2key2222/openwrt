GT_STATUS miscConfiguration(void)
{
  GT_STATUS rc = GT_OK;
  GT_U32 ii, registerAddr, regValue;
  

  /* SDMA Configuration Register */
  rc = hwPpWriteRegister(0, 0x00002800, 0x800000F8);
  if(rc != GT_OK)
    return rc;

  /* RxDMA to physical port mapping. */
  /* Assign source port number 90 to packet received on DMA 62 */
  rc = hwPpWriteRegister(0, 0x68000950+(62*4), 90);
  if(rc != GT_OK)
    return rc;
  
  /* TxQ Port to DMA Port number mapping. */
  /* TxQ to DMA Port Map Enable.*/
  rc = hwPpWriteRegister(0, 0x40000000, 0x57E);
  if(rc != GT_OK)
    return rc;

  for(ii = 0; ii < 72 ; ii++)
  {
    rc = hwPpWriteRegister(0, 0x400002C0+4*ii, ii);
    if(rc != GT_OK)
      return rc;
  }

  /* The queuing port number for this target physical port. */
  for(ii = 0; ii < 48 ; ii++)
  {
    rc = hwPpWriteRegister(0, 0x3B900000+4*ii, ii);
    if(rc != GT_OK)
      return rc;
  }
  ii=90;
  rc = hwPpWriteRegister(0, 0x3B900000+4*ii, 62);
  if(rc != GT_OK)
    return rc;
  
  /* Remaps port to its corresponding MAC number. */  
  for(ii = 0; ii < 32 ; ii++)
  {
    registerAddr = 0x35010120+ii*4; /* Port Remap register */
    regValue = ((ii*4+0)&0xFF) | (((ii*4+1)&0xFF)<<8) | (((ii*4+2)&0xFF)<<16) | (((ii*4+3)&0xFF)<<24);
    rc = hwPpWriteRegister(0, registerAddr, regValue );
    if(rc != GT_OK)
      return rc;
  }
  /* Remaps port 90 to MAC number 62. */
  registerAddr = 0x35010178; /* Port Remap register */
  regValue = 0x5B3E5958;
  rc = hwPpWriteRegister(0, registerAddr, regValue );
  if(rc != GT_OK)
    return rc;
  
  
  return rc;
}


