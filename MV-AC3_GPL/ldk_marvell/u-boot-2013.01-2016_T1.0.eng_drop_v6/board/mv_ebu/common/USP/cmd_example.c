  /*eVLAN Spanning Table */
  entryBaseAddr = EGRESS_BRG_EVLAN_SPG_TBL_BASE_ADDR + vid*EGRESS_BRG_EVLAN_SPG_TBL_STEP;
  rc = hwPpWriteRegister(0, entryBaseAddr, SpanStateGroupIndex);
