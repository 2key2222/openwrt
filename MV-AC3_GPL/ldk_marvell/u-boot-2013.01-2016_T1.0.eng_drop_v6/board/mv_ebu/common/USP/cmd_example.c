#define MAX_NUM_OF_SMI_BUS  4
#define INVALID_SMI_BASE  0x5a5a5a5a

typedef struct SMI_info_STCT{
	GT_U32 smiBase;	  /* SMI Base address */
	GT_U32 firstPort; /* First port of 16 */
	GT_U32 lastPort;  /* Last  port of 16 */
	GT_U32 portStep;  /* Step size between ports */	
}SMI_info_STC;

static GT_STATUS PHY_Sequence_configuration
(
   SMI_info_STC  *smiInfo,
   GT_U32        *smiRegDataSeq,
   GT_U32        regInfoElements
)
{
  GT_STATUS rc = GT_OK;
  GT_U32  regInfoIndex=0, smiIdx;
  GT_U32  smiAddress, smiData, portStep;
  GT_U32  portIdx, firstPort, lastPort;
  
  for(smiIdx = 0 ; smiIdx < MAX_NUM_OF_SMI_BUS ; smiIdx++)
  {
    smiAddress = smiInfo[smiIdx].smiBase;
    if(INVALID_SMI_BASE == smiAddress) /* Last element */
		break; 
	firstPort  = smiInfo[smiIdx].firstPort;
    lastPort   = smiInfo[smiIdx].lastPort;
	portStep   = smiInfo[smiIdx].portStep;
/*    osPrintf("\n PHY sequence: SMI base=0x%X, firstPort %d, lastPort %d, portStep 0x%X\n",
	            smiAddress, firstPort, lastPort, portStep);*/
	
    for(regInfoIndex = 0 ; regInfoIndex < regInfoElements ; regInfoIndex++)
    {
      for(portIdx = firstPort ; portIdx <= lastPort ; portIdx++)
      {
        smiData = smiRegDataSeq[regInfoIndex] + portStep*portIdx;
/*        osPrintf("{0x%X, 0x%8X},\n",smiAddress, smiData);*/
        rc = hwPpWriteRegister(0, smiAddress, smiData);
        if(rc != GT_OK)
          return rc;
        osTimerWkAfterDelay(10);
      }
    }
  }

/*  osPrintf("\n PHY sequence:  loops=%d, expected loops %d\n",regInfoIndex, regInfoElements);*/
  return rc;
}

